const BEIJING_AREA = ["北京"];
const BEIJING_COORD = { x: 824, y: 195 };

/**
 * 企業の hq フィールドから地図座標を取得
 * @param {string} hq - "カリフォルニア州サンフランシスコ" のような文字列
 * @param {string} country - "アメリカ合衆国" のような文字列
 * @returns {{ x: number, y: number, city: string } | null}
 */
export function getCompanyCoord(hq, country) {
  if (!hq || hq === "非公開") return null;

  for (const [city, coord] of Object.entries(CITY_COORDS)) {
    if (hq.includes(city)) {
      // 同一エリアの企業は代表座標に集約
      if (SF_BAY_AREA.includes(city)) return { ...SF_COORD, city: "サンフランシスコ" };
      if (SEATTLE_AREA.includes(city)) return { ...SEATTLE_COORD, city: "シアトル" };
      if (NY_AREA.includes(city)) return { ...NY_COORD, city: "ニューヨーク" };
      if (BEIJING_AREA.includes(city)) return { ...BEIJING_COORD, city: "北京" };
      return { ...coord, city };
    }
  }

  // 国名フォールバック（都市が見つからない場合）
  const COUNTRY_FALLBACK = {
    "アメリカ合衆国": { x: 180, y: 200, city: "アメリカ" },
    "中国": { x: 824, y: 210, city: "中国" },
    "イギリス": { x: 500, y: 181, city: "ロンドン" },
    "フランス": { x: 507, y: 187, city: "パリ" },
    "ドイツ": { x: 521, y: 186, city: "ドイツ" },
    "カナダ": { x: 207, y: 183, city: "カナダ" },
    "オランダ": { x: 513, y: 179, city: "オランダ" },
  };

  const cleaned = country?.replace(/（.*）/, "").trim();
  if (cleaned && COUNTRY_FALLBACK[cleaned]) return { ...COUNTRY_FALLBACK[cleaned] };

  return null;
}
