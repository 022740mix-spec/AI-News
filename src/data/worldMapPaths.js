// SF ベイエリアの都市を1つの座標に集約
const SF_BAY_AREA = ["サンフランシスコ", "サンノゼ", "マウンテンビュー", "メンロパーク", "クパチーノ"];
const SF_COORD = { x: 162, y: 196 };

// シアトル圏
